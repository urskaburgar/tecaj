mood = input("What mood are you in? ")

if mood == "happy":
    print("It is great to see you happy! ")
elif mood == "nervous":
    print("Take a deep breath 3 times. ")
elif mood == "sad":
    print("Sorry to hear that. Make yourself some pancakes to cheer up. ")
elif mood == "excited":
    print("Awesome. ")
elif mood == "relaxed":
    print("Cool. You're living your best life. ")
else:
    print("I don't recognize this mood")

